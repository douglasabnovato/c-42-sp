#!/usr/bin/env python3

prim_num = int(input("Me dê o primeiro número: "))

segn_num = int(input("Me dê o segundo número: "))

print("Obrigado!")

print(f"{prim_num} + {segn_num} = {prim_num + segn_num}")
print(f"{prim_num} - {segn_num} = {prim_num - segn_num}")
print(f"{prim_num} / {segn_num} = {prim_num / segn_num}")
print(f"{prim_num} * {segn_num} = {prim_num * segn_num}")